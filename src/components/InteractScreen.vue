<template>
  <div class="screen">
    <div
      class="screen_inner"
      :style="{width:`${((((920-16*4)/Math.sqrt(cardsContext.length)-16)*3)/4+16)* Math.sqrt(cardsContext.length)}px`,}"
    >
      <card-flip
        v-for="(card, index) in cardsContext"
        :key="index"
        :imgBackFaceUrl="`image/${card}.png`"
        :card="{ index, value: card }"
        :ref="`card-${index}`"
        :cardsContext="cardsContext"
        @onFlip="checkRule($event)"
      />
    </div>
  </div>
</template>

<script>
import CardFlip from "./Card.vue";
export default {
  name: "InteractScreen",
  props: {
    cardsContext: {
      type: Array,
      default: function () {
        return [];
      },
    },
  },
  components: { CardFlip },
  data() {
    return {
      rules: [],
    };
  },
  methods: {
    checkRule(card) {
      if (this.rules.length === 2) return false;
      this.rules.push(card);
      if (
        this.rules.length == 2 &&
        this.rules[0].value === this.rules[1].value
      ) {
        this.$refs[`card-${this.rules[0].index}`].onEnableDisableMode();
        this.$refs[`card-${this.rules[1].index}`].onEnableDisableMode();
        //reset rules []
        this.rules = [];
        const disabledElements = document.querySelectorAll(
          ".screen .card.disabled"
        );

        if (
          disabledElements &&
          disabledElements.length == this.cardsContext.length - 2
        ) {
          console.log("disable " + disabledElements);
          setTimeout(() => {
            this.$emit("onFinish");
          }, 920);
        }
      } else if (
        this.rules.length === 2 &&
        this.rules[0].value !== this.rules[1].value
      ) {
        setTimeout(() => {
          //close two  flip
          this.$refs[`card-${this.rules[0].index}`].onFlipBackCard();
          this.$refs[`card-${this.rules[1].index}`].onFlipBackCard();
          //reset rules []
          this.rules = [];
        }, 700);
      } else {
        return false;
      }
      console.log(this.rules);
    },
  },
};
</script>

